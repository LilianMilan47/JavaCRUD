    private void bouton_seConnecterMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bouton_seConnecterMouseClicked
        // TODO add your handling code here:
        Connection con;
        String url = "jdbc:mysql://localhost:3306/gestion_utilisateur";
        String user = "mysql";
        String pass = "azerty";
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection(url, user, pass);

            Statement s = con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_READ_ONLY);
            ResultSet result = s.executeQuery("SELECT * FROM utilisateur WHERE ut_pseudo = '" + jTextField_Login.getText() + "' AND ut_mp = '" + jTextField_MotDePasse.getText() + "'");
            result.last();
            int nbLigne = result.getRow();
            result.beforeFirst();
            if(nbLigne == 1){
                Mainform mainform = new Mainform();
                mainform.setVisible(true);
                this.dispose();
            } else {
                jLabel_Erreur.setText("Erreur de connection, veuillez r√©essayer !");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_bouton_seConnecterMouseClicked

